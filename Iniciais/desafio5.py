ac = input('Digite alguma coisa: ')
print(type(ac)) #mostra o tipo
print(ac.isalnum())#não sei
print('é string: ',ac.isalpha())
print('Está maiúsculo: ',ac.isupper())
print('Está capitalizada: ',ac.istitle()) #Capitalizada é quando a primeira letra é maiúscula
print('Está em minúscula: ',ac.islower())
print('É um número: ',ac.isnumeric()) 
print('Se é um espaço: ',ac.isspace()) #Só um espaço e mandou
